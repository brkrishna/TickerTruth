"""
BundlePackager — assembles delivery bundles for each pricing tier.

A bundle is a zip archive containing:
  data/            — tier-appropriate CSV or Parquet data files
  LICENSE.md       — usage terms for the purchased tier
  README.md        — field definitions and sample queries
  MANIFEST.json    — metadata (checksums, row counts, date, tier)

Tier definitions are read from config.yaml.
Data files are sourced from data/samples/ (pre-generated by SampleGenerator).
"""

import hashlib
import json
import logging
import zipfile
from datetime import date
from pathlib import Path

import yaml

logger = logging.getLogger(__name__)

PROJECT_ROOT = Path(__file__).parent.parent.parent
CONFIG_PATH  = Path(__file__).parent / "config.yaml"
SAMPLES_DIR  = PROJECT_ROOT / "data" / "samples"
BUNDLES_DIR  = PROJECT_ROOT / "releases" / "bundles"

# Files that belong in each tier bundle (matched by glob against SAMPLES_DIR)
_TIER_FILE_PATTERNS: dict[str, list[str]] = {
    "explorer":     ["public/*.csv"],
    "starter":      ["public/*.csv", "paid_tier_1/*.parquet", "paid_tier_1/*.csv"],
    "professional": ["public/*.csv", "paid_tier_1/*.parquet", "paid_tier_1/*.csv",
                     "paid_tier_2/*.parquet"],
    "enterprise":   ["public/*.csv", "paid_tier_1/*.parquet", "paid_tier_1/*.csv",
                     "paid_tier_2/*.parquet"],
}

